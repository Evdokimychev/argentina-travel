export function toPlaceListing(place: SeedPlace): PlaceListing {
  return {
    id: place.id,
    slug: place.slug,
    name: place.name,
    shortDescription: place.shortDescription,
    category: place.category,
    region: place.region,
    province: place.province,
    city: place.city,
    latitude: place.latitude,
    longitude: place.longitude,
    coverImage: place.coverImage,
    tags: place.tags,
    rating: place.rating,
    visitDuration: place.visitDuration,
    season: place.season,
    ticketPrice: place.ticketPrice,
    popularity: place.popularity,
  };
}

export function getAllPlaceListings(): PlaceListing[] {
  return PLACES_SEED.map(toPlaceListing);
}

export function getPlaceBySlug(slug: string): SeedPlace | undefined {
  return PLACES_SEED.find((p) => p.slug === slug);
}

export function getPlacesBySlugs(slugs: string[]): PlaceListing[] {
  const set = new Set(slugs);
  return PLACES_SEED.filter((p) => set.has(p.slug)).map(toPlaceListing);
}

