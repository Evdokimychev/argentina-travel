/**
 * @param {string} articleId
 * @returns {StockMediaSlot[]}
 */
function gallerySlots(articleId) {
  const config = RICH_ARTICLE_GALLERIES[articleId];
  if (!config) throw new Error(`No gallery config for article: ${articleId}`);
  const { placeId, slots } = config;
  return slots.map((slot, index) => ({
    id: `blog-rich-${articleId}-gallery-${index + 1}`,
    query: slot.query,
    wikimediaQuery: slot.wikimediaQuery,
    preferWikimedia: true,
    fallbackIds: slot.fallbackIds,
    copyFrom: `media/places/${placeId}/${slot.copyFrom}.jpg`,
    role: "gallery",
    category: "national-park",
    alt: `Национальный парк — ${slot.label}`,
    localPath: `media/blog/rich/${articleId}/gallery-${index + 1}.jpg`,
    articleId,
  }));
}

/**
 * @param {string} slug
 * @param {Partial<StockMediaSlot> & Pick<StockMediaSlot, "query" | "alt" | "category">} spec
 * @returns {StockMediaSlot}
 */
function guideHeroSlot(slug, spec) {
  return {
    id: `guide-${slug}-hero`,
    guideTopicId: slug,
    localPath: `media/guide/${slug}/hero.jpg`,
    role: "hero",
    ...spec,
  };
}

