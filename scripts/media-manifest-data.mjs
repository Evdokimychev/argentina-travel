const W = "https://upload.wikimedia.org/wikipedia/commons/thumb";

function asset({
  id,
  title,
  alt,
  caption,
  category,
  tags,
  file,
  width = 1280,
  license,
  author,
  localPath,
  placeId,
  destinationId,
  blogCategory,
  role = "gallery",
}) {
  return {
    id,
    title,
    alt,
    caption,
    category,
    tags,
    source: "wikimedia",
    sourceUrl: `${W}/${file}/${width}px-${file.split("/").pop()}`,
    license,
    author,
    localPath,
    placeId,
    destinationId,
    blogCategory,
    role,
  };
}

